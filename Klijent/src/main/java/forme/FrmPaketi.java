    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int index = tblPaketi.getSelectedRow();
        if (index != -1) {
            try {
                Paket p = (Paket) mtp.vratiPaket(index);
                FrmDodajPaket fdp = new FrmDodajPaket(this,p);
                fdp.setVisible(true);
                this.setVisible(false);
            } catch (Exception ex) {
                Logger.getLogger(FrmPaketi.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selektujte paket koji želite da izmenite!", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

