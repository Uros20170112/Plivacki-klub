    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String pretraga = txtPretraga.getText().trim();
            if (pretraga.isEmpty()) {
                srediTabelu();
                return;
            }
            List<AbstractObject> paketi = Kontroler.getInstance().pretraziPakete(pretraga);
            if (paketi.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Ne postoji paket po "
                        + "zadatoj vrednosti!", "Greška!", JOptionPane.ERROR_MESSAGE, null);
                return;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Sistem je našao pakete koji zadovoljavaju zadate vrednosti", "", JOptionPane.INFORMATION_MESSAGE);
                mtp.setPaketi(paketi);
                mtp.fireTableDataChanged();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Greška!", JOptionPane.ERROR_MESSAGE, null);
            Logger.getLogger(FrmClanovi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

